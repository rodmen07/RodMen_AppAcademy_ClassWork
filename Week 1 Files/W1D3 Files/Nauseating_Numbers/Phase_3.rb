def matrix_addition_reloaded(*matrices)
    matrices.each { |matrix| return nil if matrices[0].length != matrix.length }
    matrices.inject { |sum, ele| matrix_addition(sum, ele) }
end

def matrix_addition(matrix_1, matrix_2)
    new_matrix = Array.new(matrix_1.length) {Array.new(2)}
    (0...matrix_1.length).each do |i|
        (0...2).each do |j|
            new_matrix[i][j] = matrix_1[i][j] + matrix_2[i][j]
        end
    end
    new_matrix
end

