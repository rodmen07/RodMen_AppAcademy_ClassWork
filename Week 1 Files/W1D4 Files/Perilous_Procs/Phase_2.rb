def xnor_select(arr, prc_1, prc_2)
    new_arr = []
    arr.each do |ele|
        new_arr << ele if (prc_1.call(ele) && prc_2.call(ele))
        new_arr << ele if (!prc_1.call(ele) && !prc_2.call(ele))
    end
    return new_arr
end

